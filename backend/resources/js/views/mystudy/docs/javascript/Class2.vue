    <h2 class="text-h4 font-weight-bold">
      <span>Webページ・Webサイト</span>
    </h2>
    <v-container fluid>
      <p>
        サーバーというコンピュータとクライアントというコンピュータがあることを述べました。<br />
        これらはどちらも、インターネットに接続できるようになっています。<br />
        インターネットを介して情報のやり取りができるということです。
      </p>

      <p>
        これらの情報のやり取りのために、ハイパーテキストシステムを利用するのです。<br />
        ハイパーテキストシステムとは、ハイパーリンクというもので複数の文書を相互に関連づける仕組みです。<br />
        ハイパーテキストシステムで使われる文章のことをハイパーテキスト、<br />
        ハイパーリンクによって関連づけられた一連のハイパーテキストの集合をハイパーメディア、
        などとも呼びます。
      </p>

      <p>
        このハイパーテキストを記述するためにマークアップ言語を用います。<br />
        最も有名なものとしてはHTMLというものがあります。<br />
        この正式名称は、「HyperText Markup Language」。<br />そのままですね。<br />
        ハイパーテキストはマークアップ言語とその体裁を整えるCSSのようなスタイルシート。<br />
        そしてより機能性を持たせるための各種プログラミング言語を用いて作られます。
      </p>

      <p>
        このような特徴を持つページ単位の文書のことをWebページと言います。<br />
        ややこしいですが、Webページとハイパーテキストは、<br />
        「Webページにはハイパーテキストであるという特徴がある。」という関係性です。
      </p>

      <p>
        サーバーにハイパーテキスト形式で記述されたWebページを保存します。<br />
        サーバーは、先述の通り、特定の事由が無い限りインターネットに接続されています。<br />
        そのために記述し、保存されているのですから当然です。<br />
        クライアントもインターネットに接続されているため、サーバーとやりとりをすることで、<br />
        このWebページを見ることができるということです。
      </p>

      <p>
        では、具体的にどのようにクライアントとサーバーがやりとりをしているのか、見ていきましょう。
