        1990年代以降、爆発的に世界中に普及し、現在も全世界的に利用されている仕組みです。
      </p>

      <p>
        私たちは、このWebという仕組みを使って、インターネット上にある世界中の情報にアクセスしたり、<br />
        逆に世界中に情報を発信することができるようになったのです。
      </p>

      <p>
        インターネット上に情報を上げるためには、まずサーバーと呼ばれるコンピュータを用意し、<br />
        それに情報を保存します。一般にWebサーバーなどといいます。<br />
        この情報には、インターネット上の所在を示すURLが付与されます。<br />
        私たちは自分の端末(クライアント)から、このサーバーにある情報にインターネットを介して接触し、<br />
        Webブラウザと呼ばれるソフトウェアで閲覧することで情報を得ることができます。
      </p>

      <p>これら一連の仕組みをWebというのです。</p>
