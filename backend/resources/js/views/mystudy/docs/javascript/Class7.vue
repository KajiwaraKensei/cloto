    <h2 class="text-h4 font-weight-bold">
      <span> UX </span>
    </h2>
    <v-container fluid>
      <p>
        UXとはUser Experienceの略で、日本語訳は「ユーザー体験」、或いは「ユーザー経験」です。<br />
        製品や商品、今回であればWebサイトの利用を通じて、ユーザーが得る一連の体験のことです。<br />
        ここでは「ユーザー体験」で統一しておきましょう。
      </p>

      <p>
        UXは、製品の良し悪しにおいて、或いは製品から得られる体験全てにおいて、<br />
        ユーザーの主観的価値を重視する姿勢がとられています。<br />
        特に2000年ごろから産業界で注目されてきた概念です。
      </p>

      <p>
        ここで重要なのはユーザーの「主観的」価値に焦点を当てて製品設計していく必要があるということです。<br />
        特にビジネス現場における例として、高性能スマートフォンを市場に投入する際に、<br />
        技術や機能がどれほど優れているかを全面に押し出したところで、消費者のニーズには合いません。<br />
        あるいは訴求ポイントを間違えており、売れません。<br />
        そのスマートフォンから得られる体験にフォーカスを当て、製品をデザインし、体験ごと得る。<br />
        これが近年の製品、製品販売に求められるUXを考慮したビジネスと言えます。
      </p>

      <p>
        UXにこだわり抜き、大成功を収めている企業として米国のApple社があげられます。<br />
        ご存知の通り、家庭用PCの先駆けとしてのMacintosh、<br />
        すなわちMacシリーズで、家で手軽にPCを使って、様々な体験を得ることができる点を強調し、<br />
        また、製品も、これまでのPCオタクにしかわからない仕様ではなく、<br />
        GUIやマウスといった誰にでも操作できるものへとしたことで大成功を収めています。<br />
        その後iPhoneを市場に投入し、業界を激変させ、今日に至っていることは言うまでもありません。
