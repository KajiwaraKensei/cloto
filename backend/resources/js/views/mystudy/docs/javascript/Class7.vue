    <h2 class="text-h4 font-weight-bold">
      <span>配色 </span>
    </h2>
    <v-container fluid>
      <p>
        配色はWebデザインの中でもかなり重要な要素ですのであえて項を設けました。<br />
        色について学んでいきましょう。色は三つの属性から成っています。それは 色相 明度 彩度です。<br />
        色相は色の種類です。明度は色の明るさ。彩度は色の鮮やかさです。この3属性によって多くの色を作ることが可能です。<br />
        まずはこの基本を抑えましょう。画像編集ソフトなどでは、この3属性を調整して、画像データの持つ色を編集することが可能です。
      </p>

      <p>
        PCやモニター、スマホなどのディスプレイでは、<br />
        光の三原色(Red, Green, Blue)を用いたRGB(加法混色)法で様々な色を表現しています。<br />
        色を重ねるとどんどん白くなっていきます。#f6bf00のように16進数で色を表現する方法です。
      </p>

      <p>
        赤青緑などの色相に属する色を「有彩色」と言います。一方で白灰黒などの色味のないものを「無彩色」と言います。
        目に見える色は光の波長です。この波長の違いによって、色が変わっているわけです。<br />
        色はエネルギーを持っており、それを知ってか知らずか人間は文化ごとに多少の差異はあれど、<br />
        たいてい色に意味合いを求めてきました。<br />
      </p>

      <p>
        つまり色にはすでにある程度のイメージがついており、それらをうまく使うことで、<br />
        より効果的に非言語コミュニケーションを行うことができるのです。
        信号などはまさにそうですね。赤や黄色は危険や禁止、注意を表す色として広く認知されています。<br />
        信号が先なのか、色のイメージが先なのか、正確にはわかりませんが、<br />
        現代社会においてすでにそのようなイメージで認知されていることは間違いがないでしょう。
      </p>

      <p>
        また、寒色と暖色といったように色は温度的イメージ、同時に心理的イメージも持ち合わせます。<br />
        また彩度が低い色にはどこか寂しさをも覚えます。
        Web制作に限って言えば、赤い色は、警告やエラーの表示、あるいはどうしてもみてほしい重要な箇所を指し示す際に利用します。<br />
        緑色は、成功を表す色として捉えられています。スイッチのオン/オフも赤と緑で表現することが多いです。<br />
        このようにWebデザインにおける慣習のようなものもあります。
      </p>

      <p>
        Webサイト全体の色合いに秩序と意味を持たせることも重要です。プロジェクトの軸と雰囲気に合った色を選びましょう。<br />
        ロゴなどがある場合はその色を基調としてもう2色ほどアクセントカラーを用いてもいいかもしれません。<br />
        あるいは素材の色をアクセントカラーにしてもいいでしょう。<br />
        ヘッダーフッターの色、背景色、ボタンの色、フォントカラーなど全てに調和をもたらす必要があります。<br />
        同時に色の使い過ぎにも気をつけなくてはなりません。
      </p>

      <p>
        色の組み合わせや色の持つイメージについても深い理解を持つことが重要です。<br />
        Webデザイン集やWebデザインの教材、配色などで調べることでより理解が深まります。ぜひ取り組んでみましょう。<br />
        また、これはかなり玄人向けですが、色覚異常の方へ配慮した配色なども学習するとより一層成長できますよ。
      </p>
