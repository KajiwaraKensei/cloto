    <h2 class="text-h4 font-weight-bold">
      <span>Webデザインの歴史と流行 </span>
    </h2>
    <v-container fluid>
      <p>
        Webデザインの歴史はWorld Wide Web(いわゆるWeb)の誕生にまで遡ります。<br />
        1989年に誕生したWebはその後急速に発展し、現在こうして私たちがスマートフォンで手の上で閲覧したり、<br />
        自宅で容易に制作できるほどにまで身近な存在になりました。
      </p>

      <p>
        誕生来30年が経過しましたが、その過程でWebページのデザインにも当然進化と、流行がありました。<br />
        現在もそのような歴史の過程であり、当然技術の進歩に伴い表現方法やその流行は目まぐるしく変化しています。<br />
        これまでの大きな流れを知り、現在の流行を把握しておくことはWeb制作者において重要です。
      </p>

      <p>
        時代遅れのデザインや表現方法では、やはりその時代の平均的なUX・UIをも実現できない可能性があります。<br />
        閲覧者の目は厳しく、シビアにデザインの良し悪しが判断されます。<br />
        それでは大まかに歴史を見ていきましょう。
      </p>

      <p>
        Webの登場時、そのデザインはほとんどテキストによるもので、かろうじてテキストリンクに色がついていたりする程度のものでした。<br />
        当然当時としては画期的ですが、今から思えばなんとも素朴なデザインです。<br />
        また、現代ほどPC側のスペックも高くなく、またインターネット環境も脆弱だったため、非常に動作の重いWebサイトだったようです。
      </p>

      <p>
        1993年にHTMLが生まれたことで、マークアップが可能になり、一気にWebデザインというものが意識されるようになりました。<br />
        HTMLは現在でも現役であり、Web制作の基本ですね。<br />
        しかしこの当時、Webデザインという概念が広まりつつも、先の通り、通信速度の問題やPC側のスペックの問題もあり、<br />
        グラフィックデザインを適用させても、重くなりすぎて動作しないということが起きました。<br />
        スティーブに言わせれば、このデザインは「機能していない」状態ですね。
      </p>

      <p>
        1990年代後半になると、現在のAdobe社の前身企業が「Flash」というソフトを開発したことで、<br />
        Webデザインにテキスト、画像はもちろん、音声やアニメーションまで組み込むことができるようになったのです。大革命ですね。<br />
        一方で検索エンジンとの相性が悪かったFlashはSEOの観点方徐々に敬遠されていきます。2020年にはFlashが完全終了しました。
      </p>

      <p>
        1996年末にはCSSが登場しました。CSSはスタイルシートといって、HTMLなどのマークアップ言語の体裁を整えることができるものです。<br />
        CSSの登場により、Webページの構造をHTMLが、デザインをCSSが担当するようになり、より表現の幅が広がったのです。<br />
        また、役割を分担できるようになったことで、データは軽くなりました。
      </p>

      <p>
        その後、Web2.0が登場したことで、Webが端末やOSの制約を受けることなく利用することができるようになりました。<br />
        このおかげで現在は当たり前にOSXを搭載するMacからでもWindowsOSからでも、<br />
        ガラパゴス化したiモードからでも同じWebページ、デザインを見ることができるようになったのです。
      </p>

      <p>
        また、この頃から検索エンジンが飛躍的に進化を始めました。<br />
        Googleなどを中心にSEOなどの概念が広まり、Webデザインが単に視覚的に優れているだけでなく、<br />
        検索エンジンのアルゴリズムに適応させることで、検索した際に上位に表示されるような具体的な機能面にまでその担当範囲を広めました。
      </p>

      <p>
        2008年にiPhoneが登場するとまたしてもWebデザインに大変革が起きます。<br />
        PCに比べて画面サイズの小さいスマホでのWebブラウジングの機会が増えたことで、<br />
        両画面に最適化されたレスポンシブデザインを考慮する必要性が生まれたのです。<br />
        これまではPCでたくさんの情報を表示することに主眼が置かれていたため、<br />
        Yahoo!のWebブラウザのトップページのように縦にカラムがいくつにも分かれていたりするデザインが主流であったものが、<br />
        スマホでも見やすいようにシングルカラムのデザインへシフトしました。
      </p>

      <p>
        また、このようなデバイスの進化は当然、スペックの進化でもあり、また通信規格もどんどん変化していった中で、<br />
        より画素数の高い写真や重い素材もWebデザインに組み込めるようになりました。<br />
        Webサービスが一般化したことによりWebサイトの持つ役割も情報の表示にとどまらなくなてきたのです。
      </p>

      <p>
        そしてiPhone、iOSの登場はWebデザイン界に「スキューモーフィズム」というトレンドをもたらしました。<br />
        これはアイコンやボタンを立体表示させることで、それがボタンであることを明確に示したりするもので、<br />
        三次元の現実社会の感覚をWebデザインに応用させた画期的なものになりました。
      </p>

      <p>
        一方で2018年にはiOS7がスキューモーフィズムを廃止し、フラットデザインにシフトしたことで再び、業界がこの動きに追従します。<br />
        2014年にはGoogleが「マテリアルデザイン」というデアイン指針を発表します。<br />
        一見するとフラットデザインと似ているのですが、カーソルをホバーさせたときにアニメーションを持って変化を与えることでそれがボタンであることを明示したり、<br />
        要素の重なりを表現することで二次元空間に奥行きを作ったりと、より直感的な表現が可能になりました。
      </p>

      <p>
        2020年頃には「ニューモフィズム」という「スキューモフィズム」と「マテリアルデザイン」の組み合わせのようなデザインがトレンドになりました。<br />
        このように時代やWebサイトを閲覧するデバイスの変化によって、Webデザインは進化を続けています。<br />
        巨大ITベンチャーが先導して新たな流れを生み出し、業界標準を作ることもあります。<br />
        このデザインの潮流をうまく見定め、その時代、利用場面に最適なデザインをできる素養を身につけておきましょう。
      </p>

      <p>
        ここまでWebデザインの歴史を見てきました。<br />
        現代はノートPC、タブレット、スマホ、ウェアラブル端末。<br />
        最近ではARやVRなど仮想空間でのブラウジングもできるようになってきています。<br />
        そんな社会でこれからWebデザインをするにおいて、<br />
        どのようなデザイン指針を取るべきか少しでも参考になれば幸いです。
