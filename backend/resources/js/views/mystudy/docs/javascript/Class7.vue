        画像の中でも写真などの微妙な色合いやグラデーションが求められる表現ではこちらの形式が好まれます。<br />
        一方で、拡大すると画像が粗くなる特徴があります。<br />
        代表的なファイル形式としては、JPEG、PNG、GIFがあります。<br />
        それぞれに特徴があるので、調べてみてください。
      </p>

      <p>
        ベクターデータ形式とは、点や線、座標などの数式データで静止画像を表現する形式です。<br />
        拡大しても見た目を維持できます。ロゴやイラスト、テキストを表現する際にはこちらの形式が好まれます。<br />
        複雑なサイズの場合、ファイルサイズが大きくなってしまい、読み込みが遅くなります。<br />
        代表的なファイル形式にSVGがあります。<br />
        ファイル形式ごとに特徴が異なっていますので利用場面に応じて適切に使い分けましょう。
      </p>

      <p>
