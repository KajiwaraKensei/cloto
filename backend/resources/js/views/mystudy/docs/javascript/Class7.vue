        HTML5で追加されたvideoタグを使って埋めこむ方法や、<br />
        最近では一度YouTubeに動画をアップしてそれを埋め込むことも多いようです。
      </p>

      <p>
        最も主要なファイル形式はMP4形式になります。<br />
        こちらはほとんどのブラウザで利用可能ですが、古いバージョンのIEなどはMP4に対応していないため注意が必要です。
      </p>

      <p>
        iPhoneで撮影した動画はアップル社の開発したMOV形式で保存されます。<br />
        Windows OSで再生するには、専用アプリが必要です。<br />
        一度MP4に変換する必要があるでしょう。
      </p>

      <p>
        マイクロソフト社製のAVIやWMV、DVDなどで使われるVOBなどの形式もあるので、<br />
        場面に応じて必要な形式を選択し用意しましょう。必要であればファイル変換を行うべきでしょう。
